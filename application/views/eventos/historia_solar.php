        <p style="text-align: justify;"> El Solar nace como un teatro artesanal de construcción casera, esfuerzo ciudadano que comienza con la ocupación y limpieza de un terreno baldío en el periodo de septiembre a diciembre del año 2013. En ese periodo, el primer equipo fundarteño articula actividades con diversas organizaciones sociales: la organización Juventud Monseñor Romero, los estudiantes de pintura de Noé Alvarado, los estudiantes de música de Sergio Geovany Salinas y la comunidad de la Academia Mangoreana de Música de Cojutepeque; soñando entre todos tener un escenario para las expresiones artísticas que sean posibles.
        Abre sus puertas al público por vez primera el martes tres de diciembre del año 2013, con un dúo de jazzistas tecleños: Marlon Arturo de Paz y Gerardo Alvarado. Fue esta la primera de seis jornadas artísticas repletas de las expresiones invitadas: música, poesía, pintura, escultura, y por supuesto, teatro. Así se completó la agenda del primer festival artístico de seis días consecutivos, denominado “UNA SEMANA PARA FUNDARTE”. 
        Desde entonces, ha sido el único escenario en la cabecera departamental de Cuscatlán, la Ciudad Leal de Cojutepeque.
         Gracias a la colaboración, donaciones y el altruismo de la comunidad que apoya el trabajo permanente del Equipo de Voluntariado Fundarteño, de junio a julio de 2016 se renueva la construcción casera con una reforzada dotación de bambú y reciclados para un mejor techo. 
        La pujanza de las artes plantea cada vez más retos y exigencias, en la constante búsqueda de la profesionalidad artística de nuestra tierra. Se renueva el escenario, se expanden algunas áreas y se habilitan otras. Florecen las actividades de formación y se convierte El Solar en un espacio público de convivencia para la comunidad artística en el centro histórico de la Ciudad Leal de Cojutepeque.
