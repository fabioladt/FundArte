				<h3>DIFUSIÓN ARTÍSTICA</h3>
				<p style="text-align: justify;">La difusión de las artes se vuelve esencial cuando comprendemos que las expresiones artísticas son fruto de la expresión de una sociedad, indistintamente de las valoraciones técnicas o estéticas que puedan suscitar. Desde el punto de vista sociológico, las artes son una forma específica de la conciencia social y de la actividad humana, consistentes en un reflejo de la realidad a través de imágenes artísticas; constituyen uno de los procedimientos más importantes de la aprehensión estética del mundo.
				Las artes poseen, por lo tanto, un carácter primordialmente popular, ya que encarnan de manera directa o indirecta los ideales estéticos del pueblo, la concepción que éste tiene de la justicia y de la belleza, el «pathos» de la lucha revolucionaria del pueblo por la libertad y la felicidad. Según palabras del dramaturgo ruso Máximo Gorki, el pueblo es “el primer artista en el tiempo, y también por la belleza y genialidad de la obra, es el auténtico forjador de la cultura mundial”. La creación artística constituye una importante esfera de actividades de la población en general. La obra colectiva del pueblo es la base y el objetivo permanente del arte profesional; de ella extraen temas, ideas e imágenes las mejores obras artísticas.
				</p>
