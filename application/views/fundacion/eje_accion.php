				<h3>FORMACIÓN ARTÍSTICA</h3>
				<p style="text-align: justify;">En nuestro país, aunque estén incluidas dentro del plan de estudios de primaria y secundaria, las expresiones artísticas en la educación salvadoreña han sido relegadas al plano del entretenimiento, dejando de lado sus características de transformación social. 
				En FundArte Cojutepeque entendemos que la formación artística viene posterior al desarrollo de las expresiones, dejando el legado humano de transformar cada paso avanzado en la evolución o involución humana. Aun así, consideramos urgente que en nuestro municipio comprendamos las exigencias de las disciplinas artísticas, para que nuestras emergentes generaciones posean una sólida construcción que les permita sobrellevar en alguna medida las exigencias de ser un aprendiz o trabajador de las artes.
